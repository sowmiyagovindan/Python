class ArithmeticOps():
    def add(self,a,b):
        ops = a+b;
        print(ops)

    def subtract(self,a,b):
        ops = a-b;
        print(ops)

    def multiply(self, a, b):
        ops = a - b;
        print(ops)

optn = ArithmeticOps()
optn.add(2,3);
optn.subtract(2,3)
optn.multiply(10*100)